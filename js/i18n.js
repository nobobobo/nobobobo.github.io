// ── Apply language ──
function applyLang(lang) {
  const t = translations[lang];
  if (!t) return;

  // Text content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) el.textContent = t[key];
  });

  // HTML content (contains tags like <br>)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  // Placeholder attributes
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.dataset.i18nPh;
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  // Select options
  const select = document.getElementById('subject');
  if (select) {
    const values = ['', 'ml-dev', 'llm', 'pipeline', 'consulting', 'research', 'training', 'other'];
    const keys   = [
      'contact.subject-default', 'contact.subject-ml', 'contact.subject-llm',
      'contact.subject-pipeline', 'contact.subject-consulting',
      'contact.subject-research', 'contact.subject-training', 'contact.subject-other'
    ];
    const current = select.value;
    select.innerHTML = values.map((v, i) =>
      `<option value="${v}">${t[keys[i]] || ''}</option>`
    ).join('');
    select.value = current;
  }

  // html lang attribute
  document.documentElement.lang = lang === 'zh' ? 'zh-CN' : lang;

  // Update active button
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // Persist
  localStorage.setItem('nobox-lang', lang);
}

// ── Init ──
document.addEventListener('DOMContentLoaded', () => {
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => applyLang(btn.dataset.lang));
  });

  const saved = localStorage.getItem('nobox-lang') || 'ja';
  applyLang(saved);
});
